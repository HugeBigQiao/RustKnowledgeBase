//! 生命周期(Lifetime): 保障引用永远有效。
//!
//! ## 什么是生命周期 — 从机器层面理解
//!
//! 引用(&T)本质上是一个指针。指针指向某块内存, 但指针本身不知道那块内存
//! 是否还被占用、有没有被释放。C/C++ 里这导致悬垂指针(dangling pointer):
//! 指针还指着, 但内存已经还给系统了 — 访问即崩溃。
//!
//! Rust 的生命周期就是解决这个问题的: 编译器给每个引用贴一个"有效期标签",
//! 编译时检查——被引用的值是否比引用活得更久? 如果答案是"否", 编译报错。
//! 运行时零开销: 生命周期只是编译期的静态分析, 二进制里不留任何痕迹。
//!
//! 为什么叫"生命周期"? 因为标注的是"从创建到释放这段时间"。
//! 代码里用 'a 表示, 读作"生命周期 a"。'a 像时间轴上的一个区间。
//!
//! ## 'a 到底是干什么的 — 标签, 不是命令!
//!
//! 新手最大的误区: 以为写了 'a 能让什么东西"活久一点"。
//! **'a 不是命令, 不创造、不延长、不缩短任何生命周期。它只是一个标签。**
//!
//! 具体的值能活多久, 由代码的实际作用域决定 (花括号、drop 调用等),
//! 跟写不写 'a 完全无关。'a 只做一件事:
//!
//!   **把"引用之间的关系"说出来, 让编译器去检查。**
//!
//! 举个例子: 两个人已经结婚了, 你填表时写"配偶: 张三"——
//! 这句话不创造婚姻, 也不改变婚姻, 只是把已有的关系**标注**出来。
//! 'a 就是那个"配偶"标签: 不创造关系, 只描述关系, 让官方(编译器)核实。
//!
//! 再看 longest:
//!
//!   fn longest<'a>(x: &'a str, y: &'a str) -> &'a str
//!
//! 翻译成人话:
//!   "x、y、返回值这三个引用共享同一个生命周期标签 'a。
//!    编译器你帮我去检查——x 和 y 真的都活得足够久吗? 返回值
//!    真的没有越过任何一个的死亡点吗?"
//!
//! 编译器做的事:
//!   - 它本身就知道 x 活多久、y 活多久 (从花括号结构分析出来)
//!   - 'a = min(x的真实生命, y的真实生命) — 编译器自动取交集
//!   - 然后检查: 返回值的使用点 ≤ 'a 截止点?
//!   - 如果返回值企图在 'a 截止之后使用 → 编译报错
//!
//! 关键结论:
//!   'a 标注的是"约束条件", 不是"操作指令"。
//!   它说"你俩的生命周期必须满足这个关系", 而不是"我给你俩的生命周期改成这样"。
//!   真正的生命周期在花括号里, 不在 'a 里。
//!
//! ## 生命周期存在在所有 Rust 代码里
//!
//! 是的——每个引用都有生命周期。但绝大多数情况下编译器能自动推断,
//! 不用你写。这叫做"生命周期省略"(lifetime elision)。
//! 你只在编译器"猜不出来"的时候才需要手动标注。
//!
//! 什么时候编译器猜不出来? 当函数返回一个引用, 且参数里有多于一个引用时,
//! 编译器无法确定返回值和哪个参数"共享命运"。
//!
//! ## struct 字段引用也需要标注
//!
//! struct 里如果持有引用, 必须标注生命周期——因为 struct 不能比它
//! 引用的数据活得更久。
//!
//! ## 'static 生命周期
//!
//! 存活于整个程序运行期间的引用, 标注为 'static。
//! 字符串字面量("hello")天然是 &'static str — 它们编译进二进制, 永远有效。
//! static 变量也是 'static。这关联到了全局单例模式 (详见 static_and_const.rs)。
//!
//! 前置依赖: basic/ 中的 ownership_and_refs; intermediate/ 中的 static_and_const。


// ═══════════════════════════════════════════════════════════════
// 第 1 节: 为什么需要生命周期 — 悬垂指针问题
// ═══════════════════════════════════════════════════════════════
//
// C 语言里你可以这样写 (Rust 不会编译通过, 这里用注释演示):
//
//   int* dangling() {
//       int x = 42;
//       return &x;  // ❌ 返回局部变量的地址! x 在函数返回后就不存在了
//   }
//
// Rust 的 borrow checker 会在编译期就拦住:
//
//   fn dangling() -> &i32 {
//       let x = 42;
//       &x         // ❌ 编译报错: x 活不到函数外面
//   }
//
// 这个简单场景编译器能自动分析。但看下面:

/// 返回两个引用中较长的一个。
/// 不加生命周期标注会编译报错: "expected named lifetime parameter"。
///
/// 为什么编译器猜不出来?
///   编译器的规则是"只看函数签名, 不看函数体"。签名里 x 和 y 都是 &str,
///   返回值也是 &str — 编译器不知道返回值来自 x 还是 y (实际取决于运行时 if)。
///   所以它拒绝猜, 要求你显式标注三者的关系。
///
/// 加了 'a 之后:
///   x: &'a str, y: &'a str, -> &'a str  表示"x、y、返回值共享同一个生命周期 'a"。
///   编译器取 x 和 y 中较短的那个作为实际的 'a — 因为只有活到两者都还活着
///   的时刻, 返回值才安全。如果返回 x, 但 y 已经死了, x 还活着,
///   但编译器会按 y 的死亡点来限制返回值 — 这是保守但安全的做法。
fn longest<'a>(x: &'a str, y: &'a str) -> &'a str {
    if x.len() > y.len() { x } else { y }
}
// 一句话: 'a = min(生命周期_of_x, 生命周期_of_y)。
// 如果 x 活 5 秒、y 活 3 秒, 返回值最多活 3 秒 — 取交集, 不取并集。
// ⚠ 注意: 'a 只是标签, 编译器自己算 min — 不是 'a 让谁活短了, 而是编译器
// 用 'a 这个标签来标识"谁和谁同生共死", 然后按最短的那个来约束返回值。

/// 只返回第一个参数 — 返回值的生命周期显然和 x 一致, y 无关。
fn first<'a>(x: &'a str, _y: &str) -> &'a str {      // _y 不用标注: 返回值不来自它
    x
}

// ── 语法拆解: <'a> 和 &'a ──
//
//   fn longest<'a>(x: &'a str, y: &'a str) -> &'a str
//   ~~        ~~      ~~          ~~           ~~
//   关键字     ^        ^           ^            ^
//   函数名     │        │           │            │
//   <'a> 声明生命周期参数 ┘           │            │
//   &'a str = "生命周期为 'a 的字符串引用" ──┘            │
//   返回 &'a str = 返回值也用同一个生命周期 ─────────────┘
//
// 'a 就是一个名字, 可以叫 'b、'ctx、'input 等。单字母小写是惯例。
// <'a> 放在函数名和参数列表之间 (和泛型 <T> 的位置一样)。


// ═══════════════════════════════════════════════════════════════
// 第 2 节: 生命周期省略 — 隐式存在的证据
// ═══════════════════════════════════════════════════════════════
//
// Rust 编译器有三条省略规则。满足条件时自动推断生命周期, 不用手写。
// 这证明了"每个引用都有生命周期"——只是编译器帮你写了。
//
// 规则 1: 每个引用参数获得独立的生命周期。
//   编译器视角: fn foo(x: &str, y: &str) → fn foo<'a, 'b>(x: &'a str, y: &'b str)
//
// 规则 2: 如果只有一个输入生命周期, 它被赋给所有输出。
//   编译器视角: fn foo(x: &str) -> &str → fn foo<'a>(x: &'a str) -> &'a str
//
// 规则 3: 如果有 &self / &mut self, 它的生命周期赋给所有输出。
//   编译器视角: fn get(&self) -> &str → fn get<'a>(&'a self) -> &'a str

/// 根据规则 3, 以下方法能省略 — 返回值和 self 共享生命周期。
impl<'a> Excerpt<'a> {
    fn get(&self) -> &str {                          // 等价: fn get<'b>(&'b self) -> &'b str
        self.part
    }

    fn announce_and_get(&self, msg: &str) -> &str {  // msg 的周期独立(self 推给返回值)
        println!("{}", msg);
        self.part
    }
}
// 所以 longest 之所以必须写生命周期, 是因为它有两个输入引用、
// 没有 &self、返回值可能来自两个参数中的任意一个 — 编译器没法猜。


// ═══════════════════════════════════════════════════════════════
// 第 3 节: 结构体中的生命周期
// ═══════════════════════════════════════════════════════════════
//
// 结构体持有引用时必须标注生命周期 — 因为 struct 不能比它引用的数据活得更久。

/// 摘录 — 持有一个字符串引用, 所以需要 'a。
#[derive(Debug)]
struct Excerpt<'a> {                                 // <'a> 放在 struct 名后面
    part: &'a str,                                   // 这个引用活多久, struct 最多活多久
}

impl<'a> Excerpt<'a> {                               // impl 块也要声明 'a
    fn new(part: &'a str) -> Self {
        Excerpt { part }
    }
    // get 和 announce_and_get 在上面 (第 2 节)
}

// 规则: struct 的生命周期 ≤ 它持有的引用的生命周期。
// 如果 text 被释放了, excerpt 就不能再用 — 编译器强制这一点。


// ═══════════════════════════════════════════════════════════════
// 第 4 节: 'static — 贯穿全局的生命周期
// ═══════════════════════════════════════════════════════════════
//
// 'static 是最特殊的生命周期: "活到程序结束"。直到 main 返回都不释放。
//
// 哪些东西是 'static?
//   1. 字符串字面量: "hello" → &'static str (编译进二进制数据段)
//   2. static 变量: static X: i32 = 5 → 数据段里的全局变量
//   3. const 的值被内联, 本身没有生命周期, 但取字面量时产生 'static 引用
//   4. 泄露的 Box: Box::leak(Box::new(42)) → &'static i32 (永不释放)
//
// const vs static 的完整对比见 static_and_const.rs。

/// 要求 'static 参数的函数 — 只接受能活到程序结束的值。
#[allow(dead_code)]
fn require_static(msg: &'static str) {               // 只有 'static 引用才能传进来
    println!("静态消息: {}", msg);
}

// require_static("hello");      // OK: 字面量是 'static
// let s = String::from("hi"); require_static(&s);   // ❌ s 不是 'static!

// ── 'static 和全局单例 ──
// static_and_const.rs 介绍了用 OnceLock 实现全局单例:
//   static CONFIG: OnceLock<Config> = OnceLock::new();
//   fn get_config() -> &'static Config { ... }
// 返回 &'static Config 表示"这个配置一旦初始化, 永远有效"。
// 这是 'static 在实战中最常见的用法之一。


// ═══════════════════════════════════════════════════════════════
// run — 按节演示
// ═══════════════════════════════════════════════════════════════

pub fn run() {
    // ===== 第 1 节: 为什么需要 =====
    println!("===== 第 1 节: 为什么需要生命周期 =====");
    println!("防止悬垂指针: 引用不能比被引用值活得更久。");
    println!("C 里这是运行时崩溃, Rust 里这是编译期错误。");

    let s1 = String::from("hello");
    let s2 = String::from("world!");
    let result = longest(&s1, &s2);
    println!("longest(\"{}\", \"{}\") = \"{}\"", s1, s2, result);
    // result 借用了 s1 和 s2 → result 不能在 s1/s2 释放后使用

    // 演示生命周期约束 — 用时间轴标注每个变量的生灭:
    println!("\n--- 生命周期约束演示 (时间轴) ---");
    println!("时间轴: ───────────────────────────────→");
    println!("  s1  ├────────────────────────────┤  // 全程存活");
    println!("      │  let s1 = ...              ↓ drop(s1)");
    println!();
    println!("  s3  │         ├────────┤              // 只在块内存活");
    println!("      │    let s3 = ...  ↓ drop(s3)");
    println!();
    println!("  r   │         ├────────┤              // r = longest(&s1, &s3)");
    println!("      │         生命周期 = min(s1, s3) = s3 的范围");
    println!("      │         ↑ r 不能越过 s3 的 drop 点");

    {
        let s3 = String::from("hi");                 // ← s3 诞生
        let r = longest(&s1, &s3);                   // ← r 诞生, 'a = min(s1的尾, s3的尾) = s3的尾
        println!("\n块内: longest(\"{}\", \"{}\") = \"{}\"", s1, s3, r);
    }                                                 // ← s3 死亡, r 也必须死亡
    // println!("{}", r);                            // ❌ 编译报错: r 的生命已随 s3 一起结束

    println!("\nfirst: {}", first(&s1, &s2));        // 只返回第一个, 不受 s2 生命周期约束

    // ===== 第 2 节: 省略规则 =====
    println!("\n===== 第 2 节: 生命周期省略 =====");
    println!("三条规则让大多数引用不用手写生命周期:");
    println!("  规则1: 每个 &T 参数获得独立生命周期");
    println!("  规则2: 1 个输入 → 自动赋给输出");
    println!("  规则3: &self → 自动赋给输出");
    println!("只有 longest(两个输入引用, 无 &self) 这样编译器\"猜不出来\"时才需手写。");

    // ===== 第 3 节: 结构体 =====
    println!("\n===== 第 3 节: 结构体中的生命周期 =====");
    let text = String::from("Rust 是一门系统编程语言, 安全且高效.");
    let excerpt = Excerpt::new(&text[..20]);          // 借 text 的前 20 字节

    println!("摘录: {:?}", excerpt);
    println!("get(): {}", excerpt.get());
    excerpt.announce_and_get("读取摘录...");

    // excerpt 的生命周期 ≤ text 的生命周期
    // drop(text);  // 如果先释放 text, excerpt.part 就成悬垂指针 — 编译器会拦住!
    println!("text 存活中 → excerpt 安全.");

    // ===== 第 4 节: 'static =====
    println!("\n===== 第 4 节: 'static 生命周期 =====");

    // 字面量是 'static
    let literal: &'static str = "编译进二进制, 永远有效";
    println!("字面量 (&'static str): {}", literal);

    // 函数要求 'static
    require_static("这是静态字符串");                 // OK: 字面量
    // let s = String::from("hi");
    // require_static(&s);                           // ❌ s 不是 'static

    println!("\n--- 'static 与全局单例 ---");
    println!("详见 static_and_const.rs 的 OnceLock 全局单例:");
    println!("  static CONFIG: OnceLock<Config> = OnceLock::new();");
    println!("  fn get_config() -> &'static Config {{ ... }}");
    println!("返回 &'static 保证: 配置一旦初始化, 整个程序运行期间都有效。");

    // ===== 核心总结 =====
    println!("\n===== 核心总结 =====");
    println!("生命周期 = 引用的有效期标签, 编译期静态检查, 运行时零开销。");
    println!("'a      = 命名一个生命周期区间");
    println!("省略规则 = 大多数引用不用手写 (编译器自动补全)");
    println!("struct   = 持有引用时必须标注");
    println!("'static  = 程序全局 (字面量/static变量/全局单例)");
}
