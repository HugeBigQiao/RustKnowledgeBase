//! Rust 支持六种位运算符，直接操作整数的二进制位。

/// 演示 Rust 所有位运算符：与、或、异或、非、左移、右移。
pub fn run() {
    let a: u8 = 0b0000_1111; // 15
    let b: u8 = 0b1010_0101; // 165

    // -------- 按位与 &：两位都为 1 才是 1 --------
    let and = a & b; // 0000_1111 & 1010_0101 = 0000_0101 = 5
    println!("a       = {:08b} ({})", a, a);
    println!("b       = {:08b} ({})", b, b);
    println!("------------------------------------------------------------");
    println!("& (与)   : {:08b} ({})  ← 两位都为 1 才是 1", and, and);

    // -------- 按位或 |：任意一位为 1 就是 1 --------
    let or = a | b; // 0000_1111 | 1010_0101 = 1010_1111 = 175
    println!("| (或)   : {:08b} ({})  ← 任意一位为 1 就是 1", or, or);

    // -------- 按位异或 ^：两位不同才是 1 --------
    let xor = a ^ b; // 0000_1111 ^ 1010_0101 = 1010_1010 = 170
    println!("^ (异或) : {:08b} ({})  ← 两位不同才是 1", xor, xor);

    // -------- 按位非 !：0 变 1，1 变 0（注意位宽） --------
    let not_a = !a;  // !0000_1111 = 1111_0000 = 240（u8 范围内）
    println!("! (非)   : {:08b} ({})  ← 0变1，1变0（按位宽）", not_a, not_a);

    // -------- 左移 <<：所有位向左移，右侧补 0 --------
    let shl = a << 2; // 0000_1111 << 2 = 0011_1100 = 60
    println!("<< (左移) : {:08b} ({})  ← 左移 2 位，右侧补 0", shl, shl);

    // -------- 右移 >>：所有位向右移，左侧补 0（无符号） --------
    let shr = b >> 3; // 1010_0101 >> 3 = 0001_0100 = 20
    println!(">> (右移) : {:08b} ({})  ← 右移 3 位，左侧补 0", shr, shr);

    // -------- 常用位运算场景 --------
    println!("\n常用场景：");
    println!("  取某一位  : (x >> n) & 1");
    println!("  设置某位  : x | (1 << n)");
    println!("  清除某位  : x & !(1 << n)");
    println!("  翻转某位  : x ^ (1 << n)");
    println!("  乘/除 2ⁿ  : x << n（乘） / x >> n（除）");
}
