//! 模式匹配: match
//!
//! match 是 Rust 最强大的控制流, 可以拿一个值和多个模式比较.match 适合"多个候选中选一个".
//! 匹配到哪个分支就执行哪个. match 也是表达式, 整个 match 可以放在 `=` 右边, 产生一个值.

/// match 的核心特点:
/// - 编译器强制你覆盖所有可能的分支(穷尽性检查)
pub fn run() {
    let x = 3;

    // ===== 基础 match =====
    // 语法: match 值 { 模式 => 执行什么, ... }
    //   `=>` 读作"则执行", 左边是模式(pattern), 右边是执行的动作.
    //   `_`  是通配符(wildcard), 匹配所有未被上面分支捕获的值.
    println!("--- 基础 match ---");
    match x {
        1 => println!("x = 1"),
        2 => println!("x = 2"),
        3 => println!("x = 3 (匹配到了这个分支)"),
        _ => println!("x 是其他值 (_ 兜底)"), // 必须覆盖所有情况, 否则编译报错
    }

    // ===== match 是表达式: 可以赋给变量 =====
    // 整个 match 块会返回匹配到的分支的值, 所以可以放在 = 右边.
    let grade = 'B';
    let score = match grade {
        'A' => 4,
        'B' => 3,
        'C' => 2,
        'D' => 1,
        _ => 0,
    };
    println!("等级 {} -> 绩点 {} (match 作为表达式赋值)", grade, score);

    // ===== 多个模式用 | 合并 =====
    // match 里的 `|` 是"或模式"(or-pattern).
    // 只要匹配 `|` 分隔的任意一个模式, 就进入这个分支.
    // 比如 'a' | 'e' | 'i' -> 值是 'a'、'e'、'i' 中的任意一个就命中, 不需要同时满足.
    // 注意和位运算的 `|` 区分:
    //   - 位运算 `|`: 在两个整数之间用, 按位或, 结果是整数  (5 | 3 = 7)
    //   - 匹配 `|`:  在 match 分支的模式之间用, 表示"任意一个满足即可", 不是运算
    let ch = 'a';
    match ch {
        'a' | 'e' | 'i' | 'o' | 'u' => println!("'{}' 是元音字母 (| 匹配多个模式)", ch),
        _ => println!("'{}' 是辅音字母", ch),
    }

    // ===== 范围匹配 ..= =====
    // `..=` 表示一个闭区间, 包含两端. 比如 `0..=12` = 0 到 12(含).
    // 注意 `..=` 和 `..` 的区别: `..=` 包含右端, `..` 不包含右端.
    let age = 25;
    match age {
        0..=12  => println!("年龄 {}: 儿童  (0..=12)", age),
        13..=17 => println!("年龄 {}: 青少年(13..=17)", age),
        18..=59 => println!("年龄 {}: 成年人(18..=59)", age),
        60..=150 => println!("年龄 {}: 老年人(60..=150)", age),
        _ => println!("年龄 {}: 不太对吧", age),
    }

    // ===== 匹配守卫(match guard): 模式 + if 条件 =====
    // 语法: 模式 if 条件 => 执行
    //   if 条件写在模式后面, => 前面. 只有模式匹配 且 条件为 true 才走这个分支.
    //   if 只能放在 => 左边(模式这边), 不能放在 => 右边.
    //   右边是执行体, 不是条件位置.
    let num = 4;
    match num {
        n if n % 2 == 0 => println!("{} 是偶数 (守卫: n if n % 2 == 0)", n),
        n if n % 2 != 0 => println!("{} 是奇数 (守卫: n if n % 2 != 0)", n),
        _ => unreachable!(),  // 理论上不会走到, 但编译器要求穷尽
    }

    // 守卫不能放在 => 右边, 下面是错误示范:
    // match num {
    //     n => if n % 2 == 0 { ... }  // 这是 => 右边的 if 语句, 不是守卫
    // }
    // 守卫的 if 是 match 语法的一部分, 位置固定: 模式 if 条件 =>
}
